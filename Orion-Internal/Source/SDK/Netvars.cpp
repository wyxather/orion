	[[nodiscard]] auto exist(std::uint32_t hash) noexcept
	{
		if (const auto it = std::ranges::lower_bound(netvars, hash, {}, &Netvar::hash); it != netvars.cend() && it->hash == hash)
			return true;
		return false;
	}

